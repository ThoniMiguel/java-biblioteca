    private void jButtonVerTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerTodosActionPerformed
        // TODO add your handling code here:
        AuthorController authController = new AuthorController();
        List<Author> list = authController.findAll();
        DefaultListModel<Author> model = new DefaultListModel();
        for (Author author : list) {
            model.addElement(author);
        }
        jListBuscarAutor.setModel((ListModel) model);
    }//GEN-LAST:event_jButtonVerTodosActionPerformed

