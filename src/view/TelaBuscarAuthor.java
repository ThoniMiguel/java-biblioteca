    private void jButtonBuscarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarAutorActionPerformed
        // TODO add your handling code here:
        AuthorController authorController = new AuthorController();
        String name = jTextFieldNomeBuscarAutorNome.getText();
        String sobrenome = jTextFieldNomeBuscarAutorSobrenome.getText();
        Author author = authorController.buscarAutores(name, sobrenome);
        if(author == null){
            JOptionPane.showMessageDialog(null, "Sua busca nao retornou resultados!");
            return;
        }
        List<Author> temp = new ArrayList<>();
        temp.add(author);
        DefaultListModel<Author> model = new DefaultListModel<>();
        for(Author a : temp){
            model.addElement(a);
        }
        jListBuscarAutor.setModel((ListModel) model);
        
    }//GEN-LAST:event_jButtonBuscarAutorActionPerformed

