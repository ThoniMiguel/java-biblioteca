    private void jButtonAlterarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarAutorActionPerformed
        // TODO add your handling code here:
        AuthorController authorController = new AuthorController();
        String nome = jTextFieldAlterarAutorNovoNome.getText();
        String sobrenome = jTextFieldAlterarAutorNovoSobrenome.getText();
        Author newAuthor = new Author();
        newAuthor.setName(nome);
        newAuthor.setfName(sobrenome);
        try {
            boolean situacao = authorController.updateAuthor(newAuthor, this.author);
            if (situacao) {
                JOptionPane.showMessageDialog(null, "Elemento atualizado com sucesso!");
                this.dispose();
                new TelaAlterarAuthor().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Os valores inseridos est√£o incorretos. Tente novamente!");
            }
        }catch(Exception e){
            System.out.println("Oh nao :( O seguinte erro aconteceu: " + e.getMessage());
        }


    }//GEN-LAST:event_jButtonAlterarAutorActionPerformed

