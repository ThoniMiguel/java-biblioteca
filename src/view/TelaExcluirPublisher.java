    private void jButtonDeletarPublisherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarPublisherActionPerformed
        // TODO add your handling code here:
        BookController bookC = new BookController();
        PublisherController pubC = new PublisherController();
        Object temp = jListExcluirEditora.getSelectedValue();
        if(temp == null){
            JOptionPane.showMessageDialog(null, "Selecione um valor!", "Aviso", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Publisher pub = (Publisher) temp;
        List<String> list = bookC.searchBookFromPublisher(pub);
        pubC.delete(pub, list);
        this.dispose();
        new TelaPrincipal().setVisible(true);
        
    }//GEN-LAST:event_jButtonDeletarPublisherActionPerformed

